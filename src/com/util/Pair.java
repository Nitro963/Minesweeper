package com.util;

public class Pair {
	int x ,y;

	public final int getX() {
		return x;
	}

	public final void setX(int x) {
		this.x = x;
	}

	public final int getY() {
		return y;
	}

	public final void setY(int y) {
		this.y = y;
	}

	public Pair(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	
}
